package Model;

public class Traitement_score {
	
	private String mat;
	private int score;
	public Traitement_score() {
		
	}
	public Traitement_score(String mat, int score) {
		
		this.mat = mat;
		this.score = score;
	}
	public String getMat() {
		return mat;
	}
	public int getScore() {
		return score;
	}
	
}
